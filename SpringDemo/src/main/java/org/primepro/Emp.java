package org.primepro;

public class Emp {
	  private int eno;
	  private String name;
	  private String address;
	  
	  public Emp() {
		
	  }
	  
	  

	public Emp(int eno, String name, String address) {
		super();
		this.eno = eno;
		this.name = name;
		this.address = address;
	}



	public int getEno() {
		return eno;
	}



	public String getName() {
		return name;
	}



	public String getAddress() {
		return address;
	}
	
	
	



	}
